#include <stdlib.h>

#include "libft.h"

void	*ft_calloc(unsigned int nmemb, unsigned int size)
{
	void			*array;
	unsigned long	len;

	len = nmemb * size;
	if (len >= 0b11111111111111111111111111111111)
		return (NULL);
	array = malloc(nmemb * size);
	if (array == NULL)
		return (NULL);
	ft_bzero(array, nmemb * size);
	return (array);
}
