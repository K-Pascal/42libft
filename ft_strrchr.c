char	*strrchr(const char s[], int c)
{
	unsigned int	i;
	char			*last;

	last = 0;
	i = 0;
	while (s[i] != '\0')
	{
		if (s[i] == c)
			last = s + i;
		i++;
	}
	return (last);
}
