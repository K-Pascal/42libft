#include <stddef.h>

void	*ft_memchr(const void *s, int c, size_t n)
{
	size_t	i;

	i = 0;
	while (i < n)
	{
		if (*(unsigned char *)(s + i * sizeof(unsigned char))
				== (unsigned char)c)
			return ((void *)(s + i * sizeof(unsigned char)));
		i++;
	}
	return (0);
}
